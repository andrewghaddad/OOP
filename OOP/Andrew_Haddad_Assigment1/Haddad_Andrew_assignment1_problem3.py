# printing to match sample output
print("--------------------------------------------------------------")
print("                   mL to US Fluid Volume Units                ")
print("--------------------------------------------------------------")

#starting conversion
print("mL:          ",250.0)

# converson from mL to tsp
print("tsp:         ",250.0 * 0.202884)

# conversion from tsp to tbsp
print("tbsp:        ",(250.0 * 0.202884) /3)

# conversion from tbsp to cups
print("cup(s):      ",(250.0 * 0.202884) /3 /16)

# conversion from cups to pints
print("pint(s):     ",(250.0 * 0.202884) /3 /16 /2)

# conversion from pints to quarts
print("quart(s):    ",(250.0 * 0.202884) /3 /16 /2 /2)

# conversion from quarts to gallons
print("gallon(s):   ",(250.0 * 0.202884) /3 /16 /2 /2 /4)

# conversion from gallons to fluid oz.
print("floz:        ",(250.0 * 0.202884) /3 /16 /2 /2 /4 *128)

print("--------------------------------------------------------------")

